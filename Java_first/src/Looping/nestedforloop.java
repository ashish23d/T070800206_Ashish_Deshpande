 	package Looping;

public class nestedforloop {
	
 
	public static void main(String[] args) {
		
		int i;
		int j;
		
	 
		 
		
		for(i=1;i<=5;i++){
			for(j=1;j<=i;j++) {
				System.out.print("*");}
				System.out.println(" ");
			
		}
		for(i=-1;i>=1;i--){
			for(j=i;j>=1;j--) {
				System.out.print("*");}
				System.out.println("  ");
//	}

}
	}
	}
	

